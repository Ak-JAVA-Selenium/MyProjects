package Loops_In_JAVA;
								// Loops........For Loop
public class test {

	public static void main(String[] args) {

		for (int i = 1; i < 101; i++) {
			System.out.print(i+" ");
		}
		System.out.println("\n*************Reverse****************");
		
		for (int i = 100; i > 0; i--) {
			System.out.print(i+" ");
		}
	}
}
